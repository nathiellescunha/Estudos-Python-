num = int(input('Digite um número? '))
un = num // 1 % 10
dez = num // 10 % 10
ce = num // 100 % 10
mi = num // 1000 % 10

print('Analisando o número {}'.format(num))
print('A unidade é {}'.format(un))
print('A dezena é {}'.format(dez))
print('A centena é {}'.format(ce))
print('Milha é {}'.format(mi))


